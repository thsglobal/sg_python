# Abstraction, Encapsulation, Inheritance, Polymorphism

# Abstraction - Simplify use of class via methods, provide a clear user interface

# Encapsulation - Hide the details in private fields/members, prevent end user from creating problems

# Inheritance - Can reuse a class and build on it, only need to implement new methods

# Polymorphism - Horse IS A Mammal. Same method working differently across classes